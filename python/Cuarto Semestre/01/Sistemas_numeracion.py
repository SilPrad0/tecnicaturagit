# Profundizando en los sist de num

# sist decimal
a = 10
print(f"a decimal: {a}")
# binario
a = 0b1010
print(f"a binario: {a}")
# Octal
a = 0o12
print(f"a octario: {a}")
# Hexadecimal
a = 0xA
print(f'a hexa: {a}')

# Base decimal
a = int('23', 10)
print(f"a base decimal: {a}")
# Base binario
a = int('10111', 2)
print(f"a base binario: {a}")
# Base Octal
a = int('27', 8)
print(f"a base octal: {a}")
# base hexadecimal
a = int('17', 16)
print(f"a base hexa: {a}")